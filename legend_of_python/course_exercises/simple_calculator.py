def add(a, b):
  total = a + b
  return total

def substract(a, b):
  total = a - b
  return total

def multiply(a, b):
  total = a * b
  return total

def divide(a, b):
  total = a / b
  return total

def exp(a, b):
  total = a ** b
  return total

print(add(2, 2))
print(substract(20, 6))
print(multiply(2, 3))
print(divide(121, 11))

print(exp(8, 5))
